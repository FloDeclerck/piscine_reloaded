int		ft_sqrt(int nb)
{
	int div;

	div = 1;
	if (nb <= 0)
		return (0);
	while (nb / div > div)
		div++;
	if ((nb / div) == div && (nb % div == 0))
		return (div);
	else
		return (0);
}
